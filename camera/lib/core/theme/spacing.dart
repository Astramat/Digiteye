/// Espacements standardisés de l'application
class AppSpacing {
  // Espacements de base (multiples de 4)
  static const double xs = 4.0;
  static const double sm = 8.0;
  static const double md = 16.0;
  static const double lg = 24.0;
  static const double xl = 32.0;
  static const double xxl = 48.0;
  static const double xxxl = 64.0;
  
  // Espacements personnalisés
  static const double none = 0.0;
  static const double tiny = 2.0;
  static const double small = 12.0;
  static const double medium = 20.0;
  static const double large = 28.0;
  static const double huge = 40.0;
  static const double massive = 80.0;
  
  // Padding et margin standards
  static const double paddingXS = xs;
  static const double paddingSM = sm;
  static const double paddingMD = md;
  static const double paddingLG = lg;
  static const double paddingXL = xl;
  
  static const double marginXS = xs;
  static const double marginSM = sm;
  static const double marginMD = md;
  static const double marginLG = lg;
  static const double marginXL = xl;
  
  // Espacements pour les composants
  static const double cardPadding = md;
  static const double buttonPadding = sm;
  static const double inputPadding = sm;
  static const double listItemPadding = md;
  static const double sectionPadding = lg;
  
  // Espacements pour les layouts
  static const double screenPadding = md;
  static const double containerPadding = lg;
  static const double gridSpacing = md;
  static const double columnSpacing = sm;
}
